/*
====================================================
LeetCode Problem: Power of Two
Problem Number: 231

Approach:
- Handle the special case where
  n == 1.
  - Since 2⁰ = 1, return true.

- If n is odd (except 1),
  it cannot be a power of two.
  Return false.

- Initialize a variable `x` to 1.

- Repeatedly multiply x by 2 until:
    - x becomes equal to n, or
    - x exceeds n / 2, meaning
      n cannot be a power of two.

- Return whether x equals n.

Why this approach?
- Powers of two are generated by
  repeatedly multiplying by 2:
      1, 2, 4, 8, 16, ...

- If the generated value reaches n,
  then n is a power of two.
- Otherwise, it is not.

Example:

Input:
n = 16

Sequence:
1 → 2 → 4 → 8 → 16

Output:
true

Input:
n = 18

Sequence:
1 → 2 → 4 → 8 → 16
Next value would exceed 18

Output:
false

Time Complexity: O(log n)
Space Complexity: O(1)

Where:
n = given integer

Key Concepts:
- Math
- Iteration
- Powers of Two

Note:
The optimal solution uses bit
manipulation:

    n > 0 && (n & (n - 1)) == 0

This runs in O(1) time by exploiting
the property that a power of two has
exactly one set bit in its binary
representation.

====================================================
*/


class Solution {
public:
    bool isPowerOfTwo(int n) {
        if(n==true) return true;
        if(n%2!=0){
            return false;
        }
        int x=1;
        while(x<n){
            if(x>n/2){
                return false;
            }
            x=x*2;
        }
        return x==n;
    }
};
